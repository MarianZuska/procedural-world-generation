    //perlin noise for sampling points
    private float samplePoint(Vector3 point) {
        point /= xCount;
        point *= perlinZoom;
        point.x += offsetX;
        point.y += offsetY;
        point.z += offsetZ;

        return Perlin.Noise(point);
    }

    //returns cubeIndex, where each binary bit marks whether a point is in the mesh or not
    public int index(Vector3 point) {
        int cubeIndex = 0;

        for(int i = 0; i < 8; i++) {
            //float val = Perlin3D(point + cornerOffsets[i]);
            float val = samplePoint(point+cornerOffsets[i]);
            if (val < surfaceLevel) {
                //set i'th bit if below surface
                cubeIndex |= 1 << i; 
            }
        }
        return cubeIndex;

    }

    float mod(float k, float n) {
        return ((k %= n) < 0) ? k + n : k;
    }

    private void Start() {
        //random Vector to induce new generation in first frame in update
        
        Vector3 currentSector = new Vector3(player.position.x - mod(player.position.x, xCount),
                                            player.position.y - mod(player.position.y, yCount),
                                            player.position.z - mod(player.position.z, zCount));

        meshHolders = new List<meshHolderStruct>();
        for (int x = -1; x <= 1; x++) {
            for (int y = -1; y <= 1; y++) {
                for (int z = -1; z <= 1; z++) {
                    GameObject curMeshHolder = Instantiate(meshHolder, Vector3.zero, Quaternion.identity, transform);
                    curMeshHolder.GetComponent<MeshFilter>().sharedMesh = new Mesh();
                    meshHolderStruct cur = new meshHolderStruct();
                    cur.meshHolder = curMeshHolder;
                    cur.minCoord = Vector3.zero;
                    cur.used = false;
                    cur.destroy = false;
                    meshHolders.Add(cur);
                }
            }
        }

        lastBeenSector = currentSector + new Vector3(6, 5, -3);
    }

    private void Update() { 
        //generate new sectors if player too far
        Vector3 currentSector = new Vector3(player.position.x - mod(player.position.x, xCount),
                                            player.position.y - mod(player.position.y, yCount),
                                            player.position.z - mod(player.position.z, zCount));

        //chunking (only compute 9 new chunks)
        if (Vector3.Distance(currentSector, lastBeenSector) >= 0.5f) {
            List<Vector3> vertsAll = new List<Vector3>();
            List<int> trisAll = new List<int>();

            //"deep copy" meshHolders into oldMeshHolders
            List<meshHolderStruct> oldMeshHolders = new List<meshHolderStruct>();
            for(int i = 0; i<meshHolders.Count; i++) {
                meshHolderStruct s = meshHolders[i];
                s.destroy = true;
                oldMeshHolders.Add(s);
            }
            meshHolders = new List<meshHolderStruct>();
            int meshHolderIndex = 0;
            int skippedCount = 0;
            for (int x = -1; x <= 1; x++) {
                for (int y = -1; y <= 1; y++) {
                    for (int z = -1; z <= 1; z++) {
                        Vector3 minCoord = currentSector + new Vector3(xCount * x, yCount * y, zCount * z);

                        //test if mesh already there
                        int equalMeshIndex = -1;
                        for(int i = 0; i<oldMeshHolders.Count; i++) {
                            if(Vector3.Distance(oldMeshHolders[i].minCoord, minCoord) < 0.1f && oldMeshHolders[i].used == true) {
                                //save index
                                equalMeshIndex = i;

                                //set destroy to false
                                meshHolderStruct tmp = oldMeshHolders[i];
                                tmp.destroy = false;
                                oldMeshHolders[i] = tmp;

                                break;
                            }
                        }

                        //if mesh already there
                        if (equalMeshIndex != -1) {
                            skippedCount++;
                            meshHolders.Add(oldMeshHolders[equalMeshIndex]);
                        //if new mesh needed
                        } else {
                            march(minCoord, trisAll.Count);
                            updateMesh(vertices, triangles, minCoord);
                        }
                        meshHolderIndex++;
                    }
                }
            }

            //destroy not used:
            for(int i = 0; i<oldMeshHolders.Count; i++) {
                if(oldMeshHolders[i].destroy) {
                    Destroy(oldMeshHolders[i].meshHolder);
                }
            }
            
            //updateMesh(vertsAll, trisAll);
            lastBeenSector = currentSector;
        }
    }

    public void updateMesh(List<Vector3> verts, List<int> tris, Vector3 minCoord) {
        meshHolderStruct s = new meshHolderStruct();
        s.meshHolder = Instantiate(meshHolder, Vector3.zero, Quaternion.identity, transform);
        Mesh cur = new Mesh();
        s.meshHolder.GetComponent<MeshFilter>().sharedMesh = cur;
        s.meshHolder.GetComponent<MeshCollider>().sharedMesh = cur;
        cur.Clear();
        cur.vertices = verts.ToArray();
        cur.triangles = tris.ToArray();
        cur.RecalculateNormals();

        s.meshHolder.GetComponent<MeshFilter>().sharedMesh = cur;
        s.meshHolder.GetComponent<MeshCollider>().sharedMesh = cur;

        s.destroy = false;
        s.used = true;
        s.minCoord = minCoord;
        meshHolders.Add(s);
    }

    //actual implementation of Marching Cubes Algorithm
    public void march(Vector3 minSectorPos, int trisIndexOffset) {
        float functionStart = Time.time;
        //init
        mesh = new Mesh();
        GetComponent<MeshFilter>().sharedMesh = mesh;

        vertices = new List<Vector3>();
        triangles = new List<int>();
        offsetVec = Vector3.zero;//new Vector3((float)-(xCount / 2), (float)-(yCount / 2), (float)-(zCount / 2));
        
        
        for (int x = 0; x < xCount; x ++) {
            for (int y = 0; y < yCount; y ++) {
                for (int z = 0; z < zCount; z ++) {

                    minCornerPos = minSectorPos + new Vector3(x, y, z);
                    //lookup table index
                    int lookupIndex = index(minCornerPos);

                    int nextVertIndex = 0;
                    while (triangleTable[lookupIndex, nextVertIndex] != -1) {

                        //get endpoints of edges
                        int edgeIndex = triangleTable[lookupIndex, nextVertIndex];
                        Vector3 vert1 = minCornerPos + edgeVertexOffsets[edgeIndex, 0];
                        Vector3 vert2 = minCornerPos + edgeVertexOffsets[edgeIndex, 1];


                        //interpolate:
                        float val1 = samplePoint(vert2); 
                        float val2 = samplePoint(vert1);
                        float diff;
                        if ((surfaceLevel > val1 && surfaceLevel > val2) || (surfaceLevel < val1 && surfaceLevel < val2) || val1 == val2) {
                            diff = 0.5f;
                        }
                        else {
                            diff = (surfaceLevel - val1) / (val2 - val1);
                        }
                        diff = 0.5f;
                        Vector3 mid = vert1 + diff*(vert2-vert1);
                        mid += offsetVec;
                        vertices.Add(mid);
                        triangles.Add(vertices.Count - 1 + trisIndexOffset);

                        nextVertIndex += 1;
                    }
                    Debug.Assert(nextVertIndex % 3 == 0); //always full triangles (3*x vertices)
                }
            }
        }
        Debug.Log(Time.time - functionStart);
    }

